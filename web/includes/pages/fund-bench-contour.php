                <h3>How does this make me a better investor?</h3>
                <div class="divider"></div>
                
                <div class="row">
                      <div class="col-md-4">
                          <p>This chart shows your performance versus an index over various time periods (in months). Each thin line on the contour represents 5% probability of returning that much return versus the index, with the best and worst performance for that holding period versus the market marked in green and red. The median (50% probability) is marked in blue with the average performance is marked as a thick grey line. Your current trailing performance is the black line.</p>
                          
                          <p>You can think of this chart this way: The current performance of your fund is the black line. If someone bought your fund 5 months ago, they would have gotten whatever return is marked on the chart versus the benchmark. As time passes the black line representing your trailing return will move around. The highest its ever been in the past is the green line at the top, the lowest its ever been is the red line at the bottom. Those are the outliers; 95% of the time, the black line has been above the first line above the red line, and 95% of the time its been below the first line below the green line.</p>
                          <p>Someone who invested in your fund at any point in time and held it for the given number of months would have earned on average the value shown by the thick grey line. Similarly, they would have earned a return above the blue line 50% of the time, and below the blue line 50% of the time.</p>
                          
                      </div><!--col-->
                      
                      <div class="col-md-4">
                          
                          <p>Ideally, over longer and longer time periods your contour chart should slope upwards indicating that for longer holding periods, you tend to beat the market. If your contour is broad, you are relatively volatile versus the benchmark. If the contour is narrow you are relatively less volatile versus the benchmark. The perfect fund would be very narrow with a very steep slope upward.</p>
                
                          <p>The intersection of the light blue line that represents zero return versus the index with your contour defines your success ratio versus the index which is shown on the success report.</p>
                          <p>While comparison versus the S&amp;P 500 is standard, the S&amp;P is not style neutral because it is all large and a few mid-cap stocks. For a bit more challenge you can also compare to a total market benchmark which has lately outperformed the S&amp;P 500.</p>
                          
                      </div><!--col-->
                      
                      <div class="col-md-4">
                          
                          
                
                          <p>Similarly, your fund is probably not style-neutral, you most likely have different weightings than the market by either sector or style then the market as a whole. So you can also measure your performance compared to a custom benchmark calculated by taking your weightings by style or sector every 30 days and producing a custom index blended from all the stocks in that sector. For instance, if you invested 50% of your fund in Health Care stocks and 50% in Infotech stocks, then your custom sector benchmark will be a blend of 50% of the Energy stocks in the market and 50% of the Infotech stocks in the market.</p>
                          <p>If you choose the custom benchmark, the performance of your custom benchmark versus the total market is also shown, which indicates how much of your performance over those time periods is due to allocation by style or sector.</p>
                
                          <p>Ideally, you want to pick the best stocks in the best sectors at the best time. In that case, both contours will slope upwards. If the top chart of your custom benchmark slopes upwards while the bottom one slopes downwards, that means that you're picking better-then-average stocks in the out-of-favor styles or sectors. Conversely if the top chart slopes downwards while the bottom slopes upwards, that means you're picking worse stocks in styles or sectors that are doing well.</p>
